var num1 = parseFloat(prompt("Enter first number"));

var num2 = parseFloat(prompt("Enter second number"));

var subtract = parseFloat(num1 - num2);
document.writeln("<h1>Subtraction of " + num1 + " and " + num2 + " is : " + subtract + "</h1>");
var mul = parseFloat(num1 * num2);
document.writeln("<h1>Multiplication of " + num1 + " and " + num2 + " is : " + mul + "</h1>");
var divide = parseFloat(num1 / num2);
document.writeln("<h1>division of " + num1 + " and " + num2 + " is : " + divide + "</h1>");